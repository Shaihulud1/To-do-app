import UIKit
import CoreData

class CategoryController: UITableViewController {
    
    var categories = [Category]()
    let context = (UIApplication.shared.delegate as! AppDelegate).persistentContainer.viewContext
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        loadItems()
    }

    @IBAction func addButtonPressed(_ sender: UIBarButtonItem) {
        var textField = UITextField()
        let alert = UIAlertController(title: "Add new category", message: "", preferredStyle: .alert)
        let action = UIAlertAction(title: "Add Category", style: .default) { (action) in
            if let cName = textField.text  {
                if cName != "" {
                    
                    let newCategory = Category(context: self.context)
                    newCategory.name = cName
                    self.categories.append(newCategory)
                    
                    self.saveItems()
                
                    self.tableView.reloadData()
                    
                }
            }
        }
        alert.addAction(action)
        
        alert.addTextField(configurationHandler: {(alertTextField : UITextField!) -> Void in
            alertTextField.placeholder = "Category name"
            textField = alertTextField
        })
        present(alert, animated: true, completion: nil)
    }
    
    func saveItems() {
        do {
            try context.save()
        } catch {
            print("Error while saving data")
        }
        tableView.reloadData()
    }
    
    func loadItems(request: NSFetchRequest<Category> = Category.fetchRequest()) {
        do {
            self.categories = try context.fetch(request)
        } catch  {
            print("Error while loading data")
        }
        tableView.reloadData()
    }
    
    //MARK - tableView events
    
//    override func numberOfSections(in tableView: UITableView) -> Int {
